class Maze:
    def __init__(self):
        self.grid = level_map
        self.tile_size = TILE_SIZE

    def draw(self, screen):
        for row_idx, row in enumerate(self.grid):
            for col_idx, tile in enumerate(row):
                if tile == 1:
                    pygame.draw.rect(
                        screen,
                        constants.BLUE,
                        (col_idx * self.tile_size, row_idx * self.tile_size, self.tile_size, self.tile_size)
                    )
